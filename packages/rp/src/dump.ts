import { mkdir, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { OUT } from "./config.ts";
import type { VerifiedIdToken } from "./jose.ts";
import { toHex } from "./b64.ts";
import { computePartialSha, padTailToMax, MAX_REMAINING } from "./partial_sha.ts";

export async function dumpAll(v: VerifiedIdToken): Promise<string[]> {
  await mkdir(OUT, { recursive: true });

  const written: string[] = [];
  const w = async (name: string, body: string | Uint8Array) => {
    const path = resolve(OUT, name);
    await writeFile(path, body);
    written.push(path);
  };

  // Debug / reference artifacts from the full JOSE chain.
  await w("jwe.parts.json", JSON.stringify(v.jwe, null, 2));
  await w("jws.parts.json", JSON.stringify(v.jws, null, 2));
  await w("jws.compact.txt", v.jwsCompact);
  await w("jws.header.json", JSON.stringify(v.header, null, 2));
  await w("jws.payload.json", JSON.stringify(v.payload, null, 2));

  await w("signing_input.bin", v.signingInput);
  await w("signing_input.hash.hex", toHex(v.messageHash));

  await w("signature.64.hex", toHex(v.signature64));
  await w("signature.r.hex", toHex(v.r));
  await w("signature.s.hex", toHex(v.s));

  await w("pubkey.x.hex", toHex(v.pubX));
  await w("pubkey.y.hex", toHex(v.pubY));
  await w("issuer_jwk.json", JSON.stringify(v.issuerJwk, null, 2));

  // Partial-SHA artifacts for the circuit.
  const partial = computePartialSha(v.signingInput);
  const paddedTail = padTailToMax(partial.tail);

  await w(
    "partial_sha.state.json",
    JSON.stringify(
      {
        state: partial.state,
        cutoff: partial.cutoff,
        tail_len: partial.tailLen,
        total_len: partial.totalLen,
      },
      null,
      2,
    ),
  );
  await w("partial_sha.state.hex", u32ArrayToBigEndianHex(partial.state));
  await w("signing_input.tail.bin", partial.tail);
  await w("signing_input.tail.padded.bin", paddedTail);

  await w(
    "Prover.toml",
    makeProverToml(v, partial.state, paddedTail, partial.tailLen, partial.totalLen),
  );

  return written;
}

function makeProverToml(
  v: VerifiedIdToken,
  partialState: number[],
  paddedTail: Uint8Array,
  tailLen: number,
  totalLen: number,
): string {
  if (paddedTail.length !== MAX_REMAINING) {
    throw new Error(
      `paddedTail.length ${paddedTail.length} != MAX_REMAINING ${MAX_REMAINING}`,
    );
  }
  return [
    "# Auto-generated by @singpass-zk/rp.",
    "# Drop into ../circuit/Prover.toml and run `nargo execute`.",
    "",
    `pubkey_x = ${bytesToTomlArray(v.pubX)}`,
    `pubkey_y = ${bytesToTomlArray(v.pubY)}`,
    `signature = ${bytesToTomlArray(v.signature64)}`,
    `partial_sha_state = ${u32ToTomlArray(partialState)}`,
    `signing_input_tail = ${bytesToTomlArray(paddedTail)}`,
    `signing_input_tail_len = "${tailLen}"`,
    `signing_input_total_len = "${totalLen}"`,
    "",
  ].join("\n");
}

function bytesToTomlArray(b: Uint8Array): string {
  const items = Array.from(b).map((x) => `"${x}"`).join(", ");
  return `[${items}]`;
}

function u32ToTomlArray(words: number[]): string {
  const items = words.map((w) => `"${w >>> 0}"`).join(", ");
  return `[${items}]`;
}

function u32ArrayToBigEndianHex(words: number[]): string {
  const bytes = new Uint8Array(words.length * 4);
  for (let i = 0; i < words.length; i++) {
    const w = words[i]! >>> 0;
    bytes[i * 4 + 0] = (w >>> 24) & 0xff;
    bytes[i * 4 + 1] = (w >>> 16) & 0xff;
    bytes[i * 4 + 2] = (w >>> 8) & 0xff;
    bytes[i * 4 + 3] = w & 0xff;
  }
  return toHex(bytes);
}
